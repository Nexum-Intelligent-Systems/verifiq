// Bundle verifiq-prompts/*.md into a TypeScript module so agents can load their
// prompts inside a Convex "use node" action (which cannot read repo files).
// Output is gitignored and regenerated here / in CI; the runner imports it.
import { mkdirSync, readdirSync, readFileSync, writeFileSync } from "node:fs";
import { join, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
const promptsDir = join(root, "verifiq-prompts");
const outFile = join(root, "src", "agents", "prompts.bundle.ts");

const files = readdirSync(promptsDir)
  .filter((f) => f.endsWith(".md"))
  .sort();

const entries = files
  .map((f) => `  ${JSON.stringify(f)}: ${JSON.stringify(readFileSync(join(promptsDir, f), "utf8"))},`)
  .join("\n");

const out = `/* AUTO-GENERATED by scripts/bundle-prompts.mjs — do not edit. */
/* eslint-disable */
export const PROMPT_BUNDLE: Record<string, string> = {
${entries}
};
`;

mkdirSync(dirname(outFile), { recursive: true });
writeFileSync(outFile, out);
console.log(`Bundled ${files.length} prompt files → src/agents/prompts.bundle.ts`);
